package fun.server.model.customQuery.ability;

public class abilityData {


    private Long FKeyID;

    private String FAbilityName;

    private String FAbilityTypeName;


    public Long getFKeyID() {
        return FKeyID;
    }

    public void setFKeyID(Long FKeyID) {
        this.FKeyID = FKeyID;
    }

    public String getFAbilityName() {
        return FAbilityName;
    }

    public void setFAbilityName(String FAbilityName) {
        this.FAbilityName = FAbilityName;
    }

    public String getFAbilityTypeName() {
        return FAbilityTypeName;
    }

    public void setFAbilityTypeName(String FAbilityTypeName) {
        this.FAbilityTypeName = FAbilityTypeName;
    }
}
